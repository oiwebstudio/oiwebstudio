(function () {
  const KEY = 'oi_lang';
  const nodes = document.querySelectorAll('[data-t]');
  const phNodes = document.querySelectorAll('[data-tp]');

  // Capturar el español base (una sola vez)
  nodes.forEach(el => { el.dataset._es = el.innerHTML; });
  phNodes.forEach(el => { el.dataset._esp = el.getAttribute('placeholder') || ''; });

  function apply(lang) {
    const eu = lang === 'eu';
    nodes.forEach(el => {
      const k = el.dataset.t;
      el.innerHTML = eu && EU[k] != null ? EU[k] : el.dataset._es;
    });
    phNodes.forEach(el => {
      const k = el.dataset.tp;
      el.setAttribute('placeholder', eu && EU[k] != null ? EU[k] : el.dataset._esp);
    });
    document.documentElement.lang = eu ? 'eu' : 'es';
    document.querySelectorAll('[data-lang]').forEach(b => {
      b.classList.toggle('is-active', b.dataset.lang === lang);
      b.setAttribute('aria-pressed', b.dataset.lang === lang ? 'true' : 'false');
    });
    try { localStorage.setItem(KEY, lang); } catch (e) {}
  }

  let saved = 'es';
  try { saved = localStorage.getItem(KEY) || 'es'; } catch (e) {}
  apply(saved);

  document.querySelectorAll('[data-lang]').forEach(btn => {
    btn.addEventListener('click', () => apply(btn.dataset.lang));
  });
})();
